/**
 * add - function that returns the addition
 * of two integers.
 * @a: first param.
 * @b: second param.
 * Return: result of the addition.
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - function that returns the subtraction
 * of two integers.
 * @a: first param.
 * @b: second param.
 * Return: result.
 */
int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - function that returns the multiplication
 * of two integers.
 * @a: first param.
 * @b: second param.
 * Return: result.
 */
int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - function that returns the division
 * of two integers.
 * @a: first param.
 * @b: second param.
 * Return: result.
 */
int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - function that returns the modulo
 * of two integers.
 * @a: first param.
 * @b: second param.
 * Return: result.
 */
int mod(int a, int b)
{
	return (a % b);
}
