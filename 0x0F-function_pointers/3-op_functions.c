/**
 * op_add - function that returns the addition
 * of two integers.
 * @a: first param.
 * @b: second param.
 * Return: result of the addition.
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - function that returns the subtraction
 * of two integers.
 * @a: first param.
 * @b: second param.
 * Return: result.
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - function that returns the multiplication
 * of two integers.
 * @a: first param.
 * @b: second param.
 * Return: result.
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - function that returns the division
 * of two integers.
 * @a: first param.
 * @b: second param.
 * Return: result.
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - function that returns the modulo
 * of two integers.
 * @a: first param.
 * @b: second param.
 * Return: result.
 */
int op_mod(int a, int b)
{
	return (a % b);
}
